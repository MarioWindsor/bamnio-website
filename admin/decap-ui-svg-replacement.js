/**
 * The main handler that is called when the DOM changes.
 * It runs our update logic on newly added elements.
 * @param {Node} node - The node that was added to the DOM.
 */
function handleDomChanges(node) {
  // Ensure we are working with an actual HTML element.
  if (node.nodeType === Node.ELEMENT_NODE) {
    updateNavIcons(node);
  }
}

